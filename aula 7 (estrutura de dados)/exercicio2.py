# Lista
alunos = ["Bruno", "Ana", "Carlos", "Denise", "Felipe"]

# Reorganizar em ordem invertida
alunos.reverse()
print("Alunos em ordem invertida na lista:", alunos)

# Reorganizar em ordem decrescente
alunos.sort(reverse=True)
print("Alunos em ordem decrescente:", alunos)

# Segunda saída com solicitação de posições específicas
print("Alunos escolhidos:", alunos[0],"e", alunos[3])